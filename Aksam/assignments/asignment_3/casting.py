
# integer to string
int_val = 42
str_val = str(int_val)
print("Original integer: ", int_val)
print("converted integer", str_val)


# string to float
float_string = "3.14"
float_value = float(float_string)
print("Original string", float_string)
print("converted Float", float_value)

