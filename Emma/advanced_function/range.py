#start and endpoint
for i in range(2,8):
    print(i)


#step # the last digit is the one to determine the steps to skip
for i in range(1,10,2):
    print(i)



#list comprehension
square = [x**2 for x in range(5)]
print(square)


#reversing a range 
for i in range(10,0,-2):
    print(i)
